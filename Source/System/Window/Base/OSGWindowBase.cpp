void WindowBase::onCreate(const Window *source)
{
    Inherited::onCreate(source);

    if(source != NULL)
    {

        MFViewportPtr::const_iterator PortIt  = 
            source->_mfPort.begin();
        MFViewportPtr::const_iterator PortEnd = 
            source->_mfPort.end  ();

        while(PortIt != PortEnd)
        {
            this->addPort(*PortIt);

            ++PortIt;
        }
    }
}

