{
    if(uiIndex < _mfHeightError.size())
    {
        editMField(HeightErrorFieldMask, _mfHeightError);

        MFReal32::iterator fieldIt = _mfHeightError.begin();

        fieldIt += uiIndex;
        _mfHeightError.erase(fieldIt);
    }
}

